export const THEME_KEYS = Object.keys(MAKE_SENTENCE_THEMES);

export function getRandomTheme(currentKey) {
  const others = THEME_KEYS.filter(k => k !== currentKey);
  return others[Math.floor(Math.random() * others.length)];
}

export function detectThemeCommand(text) {
  const lower = text.toLowerCase().trim();
  
  // Random theme triggers
  if (/(change|switch|new|random).*(theme|background|color|design)/i.test(lower)) {
    return { action: "random" };
  }
  if (/(theme|background|design).*(change|switch|new)/i.test(lower)) {
    return { action: "random" };
  }
  
  // Specific theme detection
  for (const key of THEME_KEYS) {
    if (lower.includes(key) || lower.includes(MAKE_SENTENCE_THEMES[key].name.toLowerCase())) {
      return { action: "specific", theme: key };
    }
  }
  
  return null;
}